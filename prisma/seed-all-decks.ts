// ─── Assemble all decks ────────────────────────────────────────────────────────
const allDecks: DeckData[] = [
  icescr,
  iccpr,
  crc,
  cedaw,
  crpd,
  cat,
  cppcg,
  icmw,
  icpped,
  yp,
];

async function main() {
  let totalCards = 0;

  for (const d of allDecks) {
    const deck = await prisma.flashcardDeck.upsert({
      where: { code: d.code },
      update: {
        titleFa: d.titleFa,
        titleEn: d.titleEn,
        description: d.description,
        sourceUrl: d.sourceUrl,
        icon: d.icon,
        order: d.order,
        rewardUsd: d.rewardUsd,
      },
      create: {
        code: d.code,
        titleFa: d.titleFa,
        titleEn: d.titleEn,
        description: d.description,
        sourceUrl: d.sourceUrl,
        icon: d.icon,
        order: d.order,
        rewardUsd: d.rewardUsd,
      },
    });

    for (let i = 0; i < d.cards.length; i++) {
      const c = d.cards[i];
      const cardId = `${d.code.toLowerCase()}-card-${i + 1}`;
      await prisma.flashcard.upsert({
        where: { id: cardId },
        update: {
          front: c.front,
          back: c.back,
          type: c.type,
          articleRef: c.articleRef,
          difficulty: c.difficulty,
          order: i + 1,
        },
        create: {
          id: cardId,
          deckId: deck.id,
          type: c.type,
          front: c.front,
          back: c.back,
          articleRef: c.articleRef,
          difficulty: c.difficulty,
          order: i + 1,
        },
      });
    }

    totalCards += d.cards.length;
    console.log(`  ✅ ${d.code}: ${d.cards.length} cards`);
  }

  console.log(`\n🎉 Seeded ${allDecks.length} decks with ${totalCards} total flashcards`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
